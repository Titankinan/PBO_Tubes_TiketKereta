    private void pilKeretaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_pilKeretaMouseClicked
        // TODO add your handling code here:
        
        pilKereta.removeAllItems();
        pilKereta.addItem("-Pilih Kereta-");
        
        String t = tujuan.getSelectedItem().toString();
        String b = berangkat.getSelectedItem().toString();
        
        int i = 0;
        while (i < Controller.Driver.a.getListKereta().size()) {
            if (Controller.Driver.a.getListKereta().get(i).getParkir().getKota() == b && 
                Controller.Driver.a.getListKereta().get(i).getTujuan().getKota() == t) {
                
                pilKereta.addItem(Controller.Driver.a.getListKereta().get(i).getKeretaID());
            }
            i++;
        }    
    }//GEN-LAST:event_pilKeretaMouseClicked

