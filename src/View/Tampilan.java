    private void konfirmasiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_konfirmasiActionPerformed
        // TODO add your handling code here:
        Kereta k = Controller.Driver.a.cariKereta(pilKereta.getSelectedItem().toString());
        
        String kelasKereta;
        if (ekonomi.isSelected()) {
            kelasKereta = "Ekonomi";
        } else {
            kelasKereta = "Eksekutif";
        }
        
        Controller.Driver.a.createPembeli(nama.getText().toString(), noKTP.getText().toString());
        Controller.Driver.a.createTransaksi(Controller.Driver.a.getListPembeli().get(Controller.Driver.a.getListPembeli().size()-1), k, Integer.valueOf(jumlahTiket.getText().toString()), kelasKereta);
        
        invoice.setText("Mohon lakukan pembayaran dengan kode: \n" 
                + k.getKeretaID() + "-" + noKTP.getText()
                + "\nTotal pembayaran: Rp." + total.getText()
                );
    }//GEN-LAST:event_konfirmasiActionPerformed

    private void cetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cetakActionPerformed
        // TODO add your handling code here:
        String kelasKereta;
        if (ekonomi.isSelected()) {
            kelasKereta = "Ekonomi";
        } else {
            kelasKereta = "Eksekutif";
        }
        
        Kereta k = Controller.Driver.a.cariKereta(pilKereta.getSelectedItem().toString());
        Transaksi t = Controller.Driver.a.getListTransaksi().get(Controller.Driver.a.getListTransaksi().size()-1);
        
        cetakTiket.setText("Nama\t: " + nama.getText() +
                           "\nKeberangkatan\t: " + berangkat.getSelectedItem() +
                           "\nTujuan\t: " + tujuan.getSelectedItem() +
                           "\nKode Kereta\t: " + pilKereta.getSelectedItem() +
                           "\nNama Masinis\t: " + k.getMasinis().getNama() +
                           "\nKelas\t: " + kelasKereta +
                           "\nTempat Duduk\t: " + t.getKursi()
        );
    }//GEN-LAST:event_cetakActionPerformed

