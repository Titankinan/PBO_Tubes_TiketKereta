        // TODO add your handling code here:
    
    if ( !"-Pilih Kota Keberangkatan-".equals(berangkat.getItemAt(berangkat.getSelectedIndex())) ) {
        tujuan.removeAllItems();
        tujuan.addItem("-Pilih Kota Tujuan-");
        Stasiun keberangkatan = Controller.Driver.a.cariStasiun(berangkat.getSelectedItem().toString());
        
        int i = 0;
        while (i < keberangkatan.getListTujuan().size()) {
            tujuan.addItem(keberangkatan.getListTujuan().get(i));
            i++;
        }
    }
